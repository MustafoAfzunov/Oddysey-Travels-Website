export function localizeTour(tour, lang = 'en') {
    const locale = (lang || 'en').split('-')[0];
    const localized = TOUR_I18N[locale]?.[tour.slug];
    if (!localized) return tour;
    return { ...tour, ...localized };
}

export function getTourBySlug(slug, lang = 'en') {
    const tour = TOURS.find((item) => item.slug === slug);
    return tour ? localizeTour(tour, lang) : null;
}
